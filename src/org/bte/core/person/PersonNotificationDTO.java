package org.bte.core.person;

public class PersonNotificationDTO {

	private int person_id;
	private int notfication_for;
	private String notfication_for_label;
	private boolean notification_sms;
	private boolean notification_email;
	public int getPerson_id() {
		return person_id;
	}
	public void setPerson_id(int person_id) {
		this.person_id = person_id;
	}
	public int getNotfication_for() {
		return notfication_for;
	}
	public void setNotfication_for(int notfication_for) {
		this.notfication_for = notfication_for;
	}
	public boolean isNotification_sms() {
		return notification_sms;
	}
	public void setNotification_sms(boolean notification_sms) {
		this.notification_sms = notification_sms;
	}
	public boolean isNotification_email() {
		return notification_email;
	}
	public void setNotification_email(boolean notification_email) {
		this.notification_email = notification_email;
	}
	public String getNotfication_for_label() {
		return notfication_for_label;
	}
	public void setNotfication_for_label(String notfication_for_label) {
		this.notfication_for_label = notfication_for_label;
	}
	
}
