package org.bte.framework.faces;

public class RFButtonDTO {

	private int button_id;
	private String button_ui_id;
	private String button_value;
	private int button_page_id;
	private int button_nav_page_id;
	private int button_component_id;
	private String button_action;
	private int button_status;
	private String button_onclick;
	private String button_oncomplete;
	
	public RFButtonDTO(){
		
	}

	public int getButton_id() {
		return button_id;
	}

	public void setButton_id(int buttonId) {
		button_id = buttonId;
	}

	public String getButton_ui_id() {
		return button_ui_id;
	}

	public void setButton_ui_id(String buttonUiId) {
		button_ui_id = buttonUiId;
	}

	public String getButton_value() {
		return button_value;
	}

	public void setButton_value(String buttonValue) {
		button_value = buttonValue;
	}

	public int getButton_page_id() {
		return button_page_id;
	}

	public void setButton_page_id(int buttonPageId) {
		button_page_id = buttonPageId;
	}

	public int getButton_component_id() {
		return button_component_id;
	}

	public void setButton_component_id(int buttonComponentId) {
		button_component_id = buttonComponentId;
	}

	public String getButton_action() {
		return button_action;
	}

	public void setButton_action(String buttonAction) {
		button_action = buttonAction;
	}

	public int getButton_status() {
		return button_status;
	}

	public void setButton_status(int buttonStatus) {
		button_status = buttonStatus;
	}

	public String getButton_onclick() {
		return button_onclick;
	}

	public void setButton_onclick(String buttonOnclick) {
		button_onclick = buttonOnclick;
	}

	public String getButton_oncomplete() {
		return button_oncomplete;
	}

	public void setButton_oncomplete(String buttonOncomplete) {
		button_oncomplete = buttonOncomplete;
	}

	public void setButton_nav_page_id(int button_nav_page_id) {
		this.button_nav_page_id = button_nav_page_id;
	}

	public int getButton_nav_page_id() {
		return button_nav_page_id;
	}
	
	
}
