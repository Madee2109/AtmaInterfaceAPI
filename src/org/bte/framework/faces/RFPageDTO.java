package org.bte.framework.faces;

public class RFPageDTO {

	private int erp_pageid;
	private String erp_pagename;
	private String erp_pagetitle;
	private String erp_pageurl;
	private String erp_pagestyle;
	public int getErp_pageid() {
		return erp_pageid;
	}
	public void setErp_pageid(int erpPageid) {
		erp_pageid = erpPageid;
	}
	public String getErp_pagename() {
		return erp_pagename;
	}
	public void setErp_pagename(String erpPagename) {
		erp_pagename = erpPagename;
	}
	public String getErp_pagetitle() {
		return erp_pagetitle;
	}
	public void setErp_pagetitle(String erpPagetitle) {
		erp_pagetitle = erpPagetitle;
	}
	public String getErp_pageurl() {
		return erp_pageurl;
	}
	public void setErp_pageurl(String erpPageurl) {
		erp_pageurl = erpPageurl;
	}
	public String getErp_pagestyle() {
		return erp_pagestyle;
	}
	public void setErp_pagestyle(String erpPagestyle) {
		erp_pagestyle = erpPagestyle;
	}
	
}
