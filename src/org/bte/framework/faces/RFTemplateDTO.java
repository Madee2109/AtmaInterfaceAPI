package org.bte.framework.faces;

public class RFTemplateDTO {
	private int rf_template_id;
	private String rf_template_name;
	private String rf_template_skin;
	private String rf_template_css;
	private String rf_template_page;
	private boolean rf_template_default;
	
	
	public int getRf_template_id() {
		return rf_template_id;
	}
	public void setRf_template_id(int rf_template_id) {
		this.rf_template_id = rf_template_id;
	}
	public String getRf_template_name() {
		return rf_template_name;
	}
	public void setRf_template_name(String rf_template_name) {
		this.rf_template_name = rf_template_name;
	}
	public String getRf_template_skin() {
		return rf_template_skin;
	}
	public void setRf_template_skin(String rf_template_skin) {
		this.rf_template_skin = rf_template_skin;
	}
	public String getRf_template_css() {
		return rf_template_css;
	}
	public void setRf_template_css(String rf_template_css) {
		this.rf_template_css = rf_template_css;
	}
	public String getRf_template_page() {
		return rf_template_page;
	}
	public void setRf_template_page(String rf_template_page) {
		this.rf_template_page = rf_template_page;
	}
	public boolean isRf_template_default() {
		return rf_template_default;
	}
	public void setRf_template_default(boolean rf_template_default) {
		this.rf_template_default = rf_template_default;
	} 
	
	
	
}
